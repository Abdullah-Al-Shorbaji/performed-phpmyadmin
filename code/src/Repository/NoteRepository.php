    public function getMaxNoteId(): int
    {
        $entityManager = $this->getEntityManager();

        $query = $entityManager->createQuery("select COALESCE(max(q.id + 1), 1) id from App\Entity\Note q");

        $result = $query->getResult();
        // returns an array of Product objects
        return $result["0"]["id"];
    }
